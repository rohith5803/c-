#include<stdio.h>
int main(){
    int a,b;
    printf("Enter any two numbers : ");
    scanf("%d %d",&a,&b);

    printf("The sum of these number is: %d\n",a+b);
    printf("The difference of these number is: %d\n",a-b);
    printf("The multiplication of these number is: %d\n",a*b);
    printf("The division quotient of these number is: %d\n",a/b);
    printf("The division reminder of these number is: %d\n",a%b);
    return 0;

}